import torch
import numpy as np

# Create a arange tensor

arange_tensor = torch.arange(4, 15, 3)
print(f"arange tensor: {arange_tensor}")
print("==============================\n")


# Create a linespace tensor

linespace_tensor = torch.linspace(6, 17, 8)
print(f"linespace tensor: {linespace_tensor}")
print("==============================\n")

